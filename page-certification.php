  <p>Anyone who is in the business of “teaching” resistance exercise, SHOULD understand biomechanics as it relates to resistance exercise. Biomechanics knowledge allows a person to select “better” exercises (more efficient, more productive and more safe), and avoid exercises that are inefficient, compromised in their degree of productivity, and potential injurious.</p>
  <p>Doug Brignole is also the author of “The Physics of Fitness”, which is endorsed by ten scholars (Ph.D. professors) and three orthopedic surgeons.  He is the leading authority on the topic of “resistance exercise, as applied to resistance exercise”.  He has over 40 years experience in the field, and numerous bodybuilding championships to his name.</p>
  <p><strong>Brignole Biomechanics</strong> is the official Trainer Certification program, which teaches the principles that allows a trainer to optimize exercise results, and minimize exercise injury risk.  This curriculum is offered internationally, in multiple languages, with the goal of standardizing the principles which lead to the best fitness results, the least amount of wasted effort, and in the safest possible manner.</p>
  <p>As one example of how an exercise can be evaluated for biomechanical efficiency, consider the following.  </p>
  <p>The amount of resistance (load) placed upon a person’s triceps (a person who weighs 180 pounds), during “Parallel Bar Dips”, is approximately 119 pounds per arm.  Conversely, the amount of resistance (load) placed upon a person’s triceps, during “Supine (flat bench) Dumbbell Triceps Extensions”—using only 20 pounds in each hand (40 pounds total) is approximately 240 pounds.  This is due to the angle of the forearm during each of these two exercises.</p>
  <p>Needless to say, it’s foolish to put out more effort and get less benefit, when a “better” option is available, which provides more benefit with less effort.</p>
  <p>The following factors determine the efficiency, productivity and safety of a resistance exercise.
</p>
<ol>
  <li>The angle of a target muscle’s “operating lever” (limb), relative to a given direction of resistance</li>
  <li>The angle at which a muscle pulls on its corresponding limb, depending on the degree of bend at the joint
</li>
  <li>The length of an “operating lever” (limb or combined limbs)
</li>
  <li>The “resistance curve”—the sequential variations of resistance, as a limb travels through an arc / range of motion
</li>
  <li>Where a limb is positioned—during an exercise—relative to the “base” or the “apex”</li>
  <li>Whether or not an exercise has “alignment”—i.e., is on the same plane as the direction of motion, and the direction of resistance, and the origin / insertion of the target muscle</li>
  <li>Whether or not the direction of resistance comes from a position that is directly opposite the origin of a target muscle</li>
  <li>Whether a joint is being moved the way it is designed to move, or it is being moved in an unnatural direction or manner</li>
  <li>Whether or not a limb is being moved directly toward a target muscle’s origin
</li>
  <li>Whether an exercise is being performed with full range of motion, partial range of motion, or no range of motion
</li>
  <li>Whether or not “bi-lateral” deficit is being avoided
</li>
  <li>Whether or not “reciprocal innervation” is being avoided</li>
  <li>Whether or not “active insuffiency” and “passive insuffiency” are being avoided</li>
  <li>Whether or not a weaker, non-target muscle interferes with (fatigues first) the adequate loading of the stronger target muscle</li>
  <li>Whether or not an exercise is being performed in a stable environment</li>
</ol>
<p>If a trainer does not thoroughly understand these factors, he (or she) is not sufficiently well-informed to “teach” resistance exercise, in the safest, most efficient and most productive manner.</p>


