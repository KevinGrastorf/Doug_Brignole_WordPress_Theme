  <p>“Biomechanics” is the study of <strong>human anatomy</strong> (the musculoskeletal system and the neurology that applies to it) and <strong>physics</strong> - combined.</p>
  <p>Any exercise that involves “resistance”—whether from free weights, cables, machines, elastic bands, body weight, etc.—involves physics, which dictates specific principles resulting in magnification of force.</p>
  <p>All anatomical movements can be evaluated by understanding the anatomy—according to what constitutes “natural” and “efficient” joint /muscle motion, and also how the Central Nervous System responds to various motions. Some motions are very safe, while others are not so safe—even dangerous.  Some motions are more productive, while others are less productive—even useless.</p>
